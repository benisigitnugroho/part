#include <iostream>
using namespace std;

int main (){
	// Mendeklarasikan variabel dan memberi nilai
	int A = 10;
	int B = 10;
	
	// Melakukan Increment
	A++; //sama dengan A+1
	
	//Melalkukan Decrement
	B--; //Sama dengan B-1
	
	//Menampilkan nilai A akhir
	cout<<"Nilai A akhir : "<<A<<endl;
	
	//Menampilkan nilai B akhir
	cout<<"Nilai B akhir : "<<B<<endl;
}